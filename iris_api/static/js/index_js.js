function inputTableFunction() {
    var x = document.getElementById("iris_input_table");
    if (x.style.display === "none") {
      x.style.display = "block";
    } else {
      x.style.display = "none";
    }
  }
  
function targetTableFunction() {
    var x = document.getElementById("iris_target_table");
    if (x.style.display === "none") {
      x.style.display = "block";
    } else {
      x.style.display = "none";
    }
  }